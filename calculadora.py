
def calculadora(n1, n2, operação):
    if (operação == 1):
        resposta = n1 + n2
    elif (operação == 2):
        resposta = n1 - n2
    elif (operação == 3):
        resposta = n1 * n2
    elif (operação == 4):
        resposta = n1 / n2
    else :
        resposta = 0
    print(resposta)

